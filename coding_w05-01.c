#include <stdio.h>  // เรียกใช้ไลบรารีมาตรฐานสำหรับการแสดงผลด้วย printf()

int main() {
    char name = 'M';         // ประกาศตัวแปร name ชนิด char เพื่อเก็บอักษรตัวแรกของชื่อเล่น เช่น A
    int age = 18;            // ประกาศตัวแปร age ชนิด int เพื่อเก็บอายุ เช่น 20 ปี
    float weight = 59.5;     // ประกาศตัวแปร weight ชนิด float เพื่อเก็บน้ำหนัก เช่น 55.5 กิโลกรัม

    // แสดงผลบรรทัดแรก: ข้อมูลทั้งหมดในรูปแบบข้อความ
    printf("Name: %c, Age: %d years old, Weight: %.1f kg\n", name, age, weight);
    // %c แสดงอักษร, %d แสดงจำนวนเต็ม, %.1f แสดงเลขทศนิยม 1 ตำแหน่ง

    // แสดงผลบรรทัดที่สอง: แยกข้อมูลแต่ละตัวแปรชัดเจน
    printf("Initial = %c\nAge = %d\nWeight = %.1f\n", name, age, weight);
    // ใช้ \n เพื่อขึ้นบรรทัดใหม่ และแสดงตัวแปรแต่ละค่าแยกกัน

    return 0;  // จบการทำงานของโปรแกรมด้วยการคืนค่า 0
}
