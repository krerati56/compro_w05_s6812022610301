#include <stdio.h>  // ใช้สำหรับฟังก์ชัน printf()

int main() {
    // บรรทัดที่ 1
    printf("a = 0617%%\n");
    // แสดงตัวแปร a มีค่า 0617 ตามด้วยเครื่องหมาย % (%% ใช้เพื่อ escape % ให้แสดงออกจริง)

    // บรรทัดที่ 2
    printf("b = 25\n");
    // แสดงตัวแปร b มีค่า 25

    // บรรทัดที่ 3
    printf("C = 19000%%\n");
    // แสดงตัวแปร C มีค่า 19000 ตามด้วยเครื่องหมาย %

    // บรรทัดที่ 4
    printf("d = 9\n");
    // แสดงตัวแปร d มีค่า 9

    // บรรทัดที่ 5
    printf("e = 12\n");
    // แสดงตัวแปร e มีค่า 12

    // บรรทัดที่ 6
    printf("f = 0001%%\n");
    // แสดงตัวแปร f มีค่า 0001 และตามด้วยเครื่องหมาย %

    // บรรทัดสุดท้าย (บรรทัดที่ 7)
    printf("%%%%4555%%\n");
    // แสดงเครื่องหมาย % ตามตำแหน่ง: %% = %, แล้วแสดง 4555 และอีก %

    return 0;  // จบโปรแกรมด้วยการคืนค่า 0
}
